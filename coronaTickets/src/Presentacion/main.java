    private void ComboBoxMesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ComboBoxMesActionPerformed
      
        //SI SELECCIONA FEBRERO 
        if(this.ComboBoxMes.getSelectedItem() == "2" ){
           if(this.ComboBoxDia.getItemCount() == 30){
               this.ComboBoxDia.removeItem("30");
               this.ComboBoxDia.removeItem("29");
           }else if(this.ComboBoxDia.getItemCount() == 31){
               this.ComboBoxDia.removeItem("31");
               this.ComboBoxDia.removeItem("30");
               this.ComboBoxDia.removeItem("29");
           } 
       }
       //SI SELECCIONA UN MES QUE TIENE 30 DIAS
       if(this.ComboBoxMes.getSelectedItem() == "4" ||  this.ComboBoxMes.getSelectedItem() == "6" ||
          this.ComboBoxMes.getSelectedItem() == "9" || this.ComboBoxMes.getSelectedItem() == "11"){

           if(this.ComboBoxDia.getItemCount() == 28){
               this.ComboBoxDia.addItem("29");
               this.ComboBoxDia.addItem("30");
           }else if(this.ComboBoxDia.getItemCount() == 31){
               this.ComboBoxDia.removeItem("31");
           } 
       }

       // SI SELECCIONA UN MES QUE TIENE 31 DIAS
       //Enero, Marzo, Mayo, Julio, Agosto, Octubre y Diciembre.
       if(this.ComboBoxMes.getSelectedItem() == "1" || this.ComboBoxMes.getSelectedItem() == "3" || this.ComboBoxMes.getSelectedItem() == "5" ||
          this.ComboBoxMes.getSelectedItem() == "7" || this.ComboBoxMes.getSelectedItem() == "8" || this.ComboBoxMes.getSelectedItem() == "10" || 
         this.ComboBoxMes.getSelectedItem() == "12"){

           if(this.ComboBoxDia.getItemCount() == 28){
               this.ComboBoxDia.addItem("29");
               this.ComboBoxDia.addItem("30");
               this.ComboBoxDia.addItem("31");
           }else if(this.ComboBoxDia.getItemCount() == 30){
               this.ComboBoxDia.addItem("31");
           } 
       }
    }//GEN-LAST:event_ComboBoxMesActionPerformed

